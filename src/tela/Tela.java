    private void jbt_inserirPerfumeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbt_inserirPerfumeActionPerformed
        // TODO add your handling code here:
        Perfume c = new Perfume();
        c.setId_perfume(Integer.parseInt(jta_id_perfume.getText())); 
        c.setVolume(Float.parseFloat(jcmbox_volume.getSelectedItem().toString()));
        c.setLote(Float.parseFloat(jta_lote.getText()));
        c.setTipoDoPerfume(jcmbox_tipoDoPerfume.getSelectedItem().toString());
        c.setValorVenda(Float.parseFloat(jta_valorVenda.getText()));
        c.setCorDoLiquido(jcmbox_corDoLiquido.getSelectedItem().toString());
        pd_operacoes.adicionarPerfume(c);
        limpartela();
        // Atualização da tabela para preservar a integridade do sistema.
        try {
            atualizaTabela();
        } catch (SQLException ex) {
            System.out.print("SQLException: "+ex.toString());
        } catch (ClassNotFoundException ex) {
            System.out.print("ClassNotFoundException: "+ex.toString());
        }
        
    }//GEN-LAST:event_jbt_inserirPerfumeActionPerformed

