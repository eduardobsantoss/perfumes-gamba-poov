    private void atualizaTabela() throws SQLException, ClassNotFoundException
    {
        jtb_cliente.setModel(dtm_tabela);
        dtm_tabela.setNumRows(0);
        listaCliente = pd_operacoes.buscarListaCompleta_cliente();
        for (Cliente p : listaCliente) 
        {
            dtm_tabela.addRow(new Object[]{p.getCpf(),p.getNome(),p.getTelefone(),p.getEndereco(),p.getEnd_insta(),p.getEnd_face(),p.getCartaoDeCredito(),p.getPix()});
	}
    }

