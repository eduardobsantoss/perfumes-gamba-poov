    private void atualizaTabela() throws SQLException, ClassNotFoundException
    {
        jtb_perfume.setModel(dtm_tabela);
        dtm_tabela.setNumRows(0);
        listaPerfume = pd_operacoes.buscarListaCompleta_perfume();
        for (Perfume p : listaPerfume) 
        {
            dtm_tabela.addRow(new Object[]{p.getId_perfume(),p.getVolume(),p.getLote(),p.getTipoDoPerfume(),p.getValorVenda(),p.getCorDoLiquido()});
	}
    }
    
