    private void jbt_salvarPdfClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbt_salvarPdfClienteActionPerformed
        // TODO add your handling code here:
        Document documento = new Document();
        PdfPTable pdft_tabela = new PdfPTable(8);
        listaCliente = pd_operacoes.buscarListaCompleta_cliente();
        for (Cliente p : listaCliente) 
        {
            
            pdft_tabela.addCell(p.getCpf());
            pdft_tabela.addCell(p.getNome());
            pdft_tabela.addCell(p.getTelefone());
            pdft_tabela.addCell(p.getEndereco());
            pdft_tabela.addCell(p.getEnd_insta());
            pdft_tabela.addCell(p.getEnd_face());
            pdft_tabela.addCell(p.getCartaoDeCredito());
            pdft_tabela.addCell(p.getPix());
            
	}
        try {
            PdfWriter.getInstance(documento, new FileOutputStream("Relatorio_Tabela_Clientes.pdf"));
            documento.open();
            documento.add(new Paragraph(" "));
            documento.add(pdft_tabela);
            System.out.print("\n PDF criado com sucesso.");
            JOptionPane.showMessageDialog(null, "PDF criado com sucesso.");
        } catch (DocumentException | FileNotFoundException ex  ) {
            System.out.print("\n Erro: "+ex.toString());
        }
        documento.close();
    }//GEN-LAST:event_jbt_salvarPdfClienteActionPerformed

