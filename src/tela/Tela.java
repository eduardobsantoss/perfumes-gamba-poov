    private void jbt_salvarPdfPerfumeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbt_salvarPdfPerfumeActionPerformed
        // TODO add your handling code here:
        Document documento = new Document();
        PdfPTable pdft_tabela = new PdfPTable(7);
        listaPerfume = pd_operacoes.buscarListaCompleta_perfume();
        for (Perfume p : listaPerfume) 
        {
            
            pdft_tabela.addCell(String.valueOf(p.getId_perfume()));
            pdft_tabela.addCell(String.valueOf(p.getVolume()));
            pdft_tabela.addCell(String.valueOf(p.getLote()));
            pdft_tabela.addCell(p.getTipoDoPerfume());
            pdft_tabela.addCell(String.valueOf(p.getValorVenda()));
            pdft_tabela.addCell(String.valueOf(p.getCorDoLiquido()));
            
	}
        try {
            PdfWriter.getInstance(documento, new FileOutputStream("Relatorio_Tabela_Perfumes.pdf"));
            documento.open();
            documento.add(new Paragraph(" "));
            documento.add(pdft_tabela);
            System.out.print("\n PDF criado com sucesso.");
            JOptionPane.showMessageDialog(null, "PDF criado com sucesso.");
        } catch (DocumentException | FileNotFoundException ex  ) {
            System.out.print("\n Erro: "+ex.toString());
        }
        documento.close();
    }//GEN-LAST:event_jbt_salvarPdfPerfumeActionPerformed

